    <script>
        document.querySelector('#search-page'). addEventListener('input', filterList);

        function filterList(){
            const searchInput = document.querySelector ('#search-page');
            const filter = searchInput.value.toLowerCase();
            const listItems = document.querySelectorAll('.search-links');

            listItems.forEach((item) =>{
                let text = item.textContent;
                if(text.toLowerCase().includes(filter.toLowerCase())){
                    item.style.display = '';
                }
                else{
                    item.style.display = 'none';
                }
            });
        }
